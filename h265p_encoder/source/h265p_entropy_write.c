static inline SINT32 XinGetMsb (
    UINT32 n)
{
    UINT32 firstSetBit;

    BIT_SCAN_REVERSE_32 (n, firstSetBit);

    return firstSetBit;
}

static void Xin265pNormalize (
    xin_cabac_struct *cabac,
    UINT32           low,
    UINT32           range)
{
    SINT32 shift;
    SINT32 oldBitCnt;
    SINT32 newBitCnt;
    UINT32 lowMask;
    UINT16 *buf;

    assert(range <= 65535U);

    oldBitCnt = cabac->bitCnt;
    shift     = 16 - 1 - XinGetMsb(range);
    newBitCnt = oldBitCnt + shift;
    buf       = cabac->preBuf;

    if (newBitCnt >= 0)
    {
        oldBitCnt += 16;
        lowMask    = (1 << oldBitCnt) - 1;

        if (newBitCnt >= 8)
        {
            *buf++ = (UINT16)(low >> oldBitCnt);

            low       &= lowMask;
            oldBitCnt -= 8;
            lowMask  >>= 8;
        }

        *buf++    = (UINT16)(low >> oldBitCnt);
        newBitCnt = oldBitCnt + shift - 24;
        low      &= lowMask;

        cabac->preBuf = buf;
    }

    cabac->low    = low << shift;
    cabac->range  = range << shift;
    cabac->bitCnt = newBitCnt;

}

static inline void Xin265pUpdateCdf (
    UINT16  *cdf,
    SINT32  val,
    int     symNum)
{
    SINT32 rate;
    SINT32 idx;
    UINT16 prob;

    assert(symNum < 17);

    rate = 3 + (cdf[symNum] > 15) + (cdf[symNum] > 31) + symNum2Speed[symNum];  // + get_msb(nsymbs);
    prob = XIN_ICDF(0);

    // Single loop (faster)
    for (idx = 0; idx < symNum - 1; ++idx)
    {
        prob = (idx == val) ? 0 : prob;

        if (prob < cdf[idx])
        {
            cdf[idx] -= ((cdf[idx] - prob) >> rate);
        }
        else
        {
            cdf[idx] += ((prob - cdf[idx]) >> rate);
        }
    }

    cdf[symNum] += (cdf[symNum] < 32);

}

static void Xin265pWriteSymbol (
    xin_cabac_struct *cabac,
    SINT32           symbol,
    UINT16           *iCdf,
    SINT32           symNum)
{
    UINT32  freqh;
    UINT32  freql;
    UINT32  low;
    UINT32  range;
    UINT32  s1Num;
    UINT32  rngh;
    UINT32  rngl;

    freql = symbol > 0 ? iCdf[symbol - 1] : XIN_ICDF(0);
    freqh = iCdf[symbol];
    low   = cabac->low;
    range = cabac->range;
    s1Num = symNum - 1;

    assert (32768U <= range);
    assert (freqh <= freql);
    assert (freql <= 32768U);
    assert (7 - XIN_EC_PROB_SHIFT - XIN_CDF_SHIFT >= 0);

    if (freql < XIN_CDF_PROB_TOP)
    {
        rngl = ((range >> 8) * (UINT32)(freql >> XIN_EC_PROB_SHIFT) >>
                (7 - XIN_EC_PROB_SHIFT - XIN_CDF_SHIFT)) +
               XIN_EC_MIN_PROB * (s1Num - (symbol - 1));
        rngh = ((range >> 8) * (UINT32)(freqh >> XIN_EC_PROB_SHIFT) >>
                (7 - XIN_EC_PROB_SHIFT - XIN_CDF_SHIFT)) +
               XIN_EC_MIN_PROB * (s1Num - (symbol + 0));

        low  += range - rngl;
        range = rngl - rngh;
    }
    else
    {
        range -= ((range >> 8) * (UINT32)(freqh >> XIN_EC_PROB_SHIFT) >>
                  (7 - XIN_EC_PROB_SHIFT - XIN_CDF_SHIFT)) +
                 XIN_EC_MIN_PROB * (s1Num - (symbol + 0));
    }

    Xin265pNormalize (
        cabac,
        low,
        range);

    Xin265pUpdateCdf (
        iCdf,
        symbol,
        symNum);

}

static void Xin265pWriteBin (
    xin_cabac_struct *cabac,
    SINT32           symbol,
    UINT16           iCdf)
{
    SINT32 prob;
    UINT32 low;
    UINT32 range;
    UINT32 freq;

    prob = (0x7FFFFF - (iCdf << 15) + iCdf) >> 8;
    low   = cabac->low;
    range = cabac->range;

    assert(0 < prob);
    assert(prob < 32768U);
    assert(32768U <= range);

    freq  = ((range >> 8) * (UINT32)(prob >> XIN_EC_PROB_SHIFT) >> (7 - XIN_EC_PROB_SHIFT));
    freq += XIN_EC_MIN_PROB;

    if (symbol)
    {
        low += range - freq;
    }

    range = symbol ? freq : range - freq;

    Xin265pNormalize (
        cabac,
        low,
        range);

}

static void Xin265pWriteBins (
    xin_cabac_struct *cabac,
    SINT32           data,
    SINT32           bits)
{
    SINT32  bit;

    for (bit = bits - 1; bit >= 0; bit--)
    {
        Xin265pWriteBin (
            cabac,
            1 & (data >> bit),
            128);
    }
}

static void Xin265pWriteGolomb (
    xin_cabac_struct *cabac,
    SINT32           symbol)
{
    SINT32 idx;
    SINT32 length;

    symbol = symbol + 1;
    idx    = symbol;
    length = 0;

    while (idx)
    {
        idx >>= 1;
        ++length;
    }

    assert(length > 0);

    for (idx = 0; idx < length - 1; ++idx)
    {
        Xin265pWriteBin (
            cabac,
            0,
            128);
    }

    for (idx = length - 1; idx >= 0; --idx)
    {
        Xin265pWriteBin (
            cabac,
            (symbol >> idx) & 0x01,
            128);
    }

}

static UINT16 Xin265pCdfElementProb (
    UINT16 *cdf,
    SINT32 element)
{
    assert(cdf != NULL);

    return (element > 0 ? cdf[element - 1] : XIN_CDF_PROB_TOP) - cdf[element];
}

void Xin265pWriteDone (
    xin_cabac_struct *cabac,
    xin_bs_struct    *bitstream)
{
    UINT8  *outBuf;
    UINT16 *preBuf;
    UINT32 mask;
    SINT32 bit;
    SINT32 bitCnt;
    UINT32 low;
    UINT32 symbol;
    SINT32 bufIdx;
    SINT32 carry;

    low    = cabac->low;
    bitCnt = cabac->bitCnt;
    outBuf = bitstream->cur;
    preBuf = cabac->preBuf;
    mask   = 0x3FFF;
    bit    = 10;
    symbol = ((low + mask) & ~mask) | (mask + 1);
    bit   += bitCnt;

    if (bit > 0)
    {
        mask = (1 << (bitCnt + 16)) - 1;

        do
        {
            *preBuf++ = (UINT16)(symbol >> (bitCnt + 16));

            symbol &= mask;
            bit    -= 8;
            bitCnt -= 8;
            mask  >>= 8;
        }
        while (bit > 0);
    }

    bufIdx = (SINT32)(preBuf - cabac->preBufBase);
    carry  = 0;
    preBuf = cabac->preBufBase;

    bitstream->cur = outBuf + bufIdx;

    /*Perform carry propagation.*/
    assert (bufIdx <= cabac->preBufSize);

    while (bufIdx > 0)
    {
        bufIdx--;

        carry          = preBuf[bufIdx] + carry;
        outBuf[bufIdx] = (unsigned char)carry;
        carry        >>= 8;
    }

}

