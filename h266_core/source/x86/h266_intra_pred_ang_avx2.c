static void Xin266ApplyAngPDPCScaleX_AVX2 (
    PIXEL    *pred,
    intptr_t predStride,
    SINT32   absAngMode,
    UINT32   angularScale,
    PIXEL    *refSide,
    SINT32   width,
    SINT32   height)
{
    SINT32  invAngleInit;
    SINT32  rowIdx;
    __m128i lftx16;
    __m128i predx16;
    __m256i lft256;
    __m256i pred256;
    __m256i difx16;
    __m256i wlx16;
    __m256i c32x16;
    __m128i vshuf;
    __m128i allZero;
    __m128i index4;
    __m128i predLo, predHi;
    SINT32  stride32;

    (void)width;

    vshuf        = _mm_loadu_si128 ((__m128i*) &intraPdpcW16Scale0Shuffle[absAngMode * 16]);
    invAngleInit = (invIntraAngTable[absAngMode] + 256) >> 9;
    c32x16       = _mm256_set1_epi16 (32);
    wlx16        = _mm256_loadu_si256 ((__m256i *)(wlScale[angularScale]));
    wlx16        = _mm256_permute4x64_epi64  (wlx16, 0);
    allZero      = _mm_setzero_si128 ();
    stride32     = (SINT32)predStride;
    index4       = _mm_set_epi32 (stride32*3, stride32*2, stride32, 0);

    for (rowIdx = 0; rowIdx < height; rowIdx += 4)
    {
        lftx16  = _mm_loadu_si128 ((__m128i*)&refSide[rowIdx + invAngleInit + 1]);
        lftx16  = _mm_shuffle_epi8 (lftx16, vshuf);
        predx16 = _mm_i32gather_epi32 ((SINT32 *)(pred), index4, 1);

        lft256  = _mm256_cvtepu8_epi16 (lftx16);
        pred256 = _mm256_cvtepu8_epi16 (predx16);
        difx16  = _mm256_sub_epi16 (lft256, pred256);
        difx16  = _mm256_mullo_epi16 (difx16, wlx16);
        difx16  = _mm256_add_epi16 (difx16, c32x16);
        difx16  = _mm256_srai_epi16 (difx16, 6);
        pred256 = _mm256_add_epi16 (pred256, difx16);

        predLo  = _mm256_castsi256_si128 (pred256);
        predHi  = _mm256_extracti128_si256 (pred256, 1);
        predx16 = _mm_packus_epi16 (predLo, predHi);

        *(UINT32*)(pred + 0 * predStride) = _mm_extract_epi32 (predx16, 0);
        *(UINT32*)(pred + 1 * predStride) = _mm_extract_epi32 (predx16, 1);
        *(UINT32*)(pred + 2 * predStride) = _mm_extract_epi32 (predx16, 2);
        *(UINT32*)(pred + 3 * predStride) = _mm_extract_epi32 (predx16, 3);

        pred += 4*predStride;

    }

}

