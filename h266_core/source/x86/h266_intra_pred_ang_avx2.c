void Xin266ExtendIntraRef_AVX2 (
    PIXEL  *refMain,
    PIXEL  *refSide,
    SINT32 intraPredAngleMode,
    SINT32 width,
    SINT32 height,
    BOOL   isModeVer)
{
    SINT32 sizeSide;
    SINT32 invAngle;

    sizeSide = isModeVer ? height : width;

    intraPredAngleMode = -intraPredAngleMode;

    switch (sizeSide)
    {
    case 4:
    {
        int shuf_offset = intraPredAngleMode * 16;
        __m128i vshuf = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_4[shuf_offset]);
        __m128i vref = _mm_loadu_si128((const __m128i*) &refSide[0]);
        vref = _mm_shuffle_epi8(vref, vshuf);
        *((SINT32 *)&refMain[-sizeSide]) = _mm_cvtsi128_si32 (vref);
        break;
    }

    case 8:
    {
        int shuf_offset = intraPredAngleMode * 16;
        __m128i vshuf = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_8[shuf_offset]);
        __m128i vref = _mm_loadu_si128((const __m128i*) &refSide[0]);
        vref = _mm_shuffle_epi8(vref, vshuf);
        _mm_storel_epi64((__m128i *)&refMain[-sizeSide], vref);
        break;
    }

    case 16:
    {
        int shuf_offset = intraPredAngleMode * 16;
        __m128i vshuf = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_16[shuf_offset]);
        __m128i vref = _mm_loadu_si128((const __m128i*) &refSide[1]); // Offset ref by one to fit all necessary 16 refs. Offset accounted for in shuffle vectors.
        vref = _mm_shuffle_epi8(vref, vshuf);
        _mm_store_si128((__m128i*) &refMain[-sizeSide], vref);
        break;
    }

    case 32:
    {
        int shuf_offset = intraPredAngleMode * 32;
        __m128i vshufhi = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_32[shuf_offset + 0]);
        __m128i vshuflo = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_32[shuf_offset + 16]);
        __m128i vblend = _mm_cmpgt_epi8(vshuflo, _mm_set1_epi8(15));

        __m128i vreflo = _mm_loadu_si128((const __m128i*) & refSide[1]); // Offset ref by one to fit all necessary 16 refs. Offset accounted for in shuffle vectors.
        __m128i vrefhi = _mm_loadu_si128((const __m128i*) & refSide[17]);

        // Second half of references requires samples from both sides
        __m128i vreftmphi = _mm_shuffle_epi8(vrefhi, vshuflo);
        __m128i vreftmplo = _mm_shuffle_epi8(vreflo, vshuflo);
        vreflo = _mm_blendv_epi8(vreftmplo, vreftmphi, vblend);

        // First half of references use references from the hi side only
        vrefhi = _mm_shuffle_epi8(vrefhi, vshufhi);

        _mm_storeu_si128((__m128i*) &refMain[-sizeSide], vrefhi);
        _mm_storeu_si128((__m128i*) &refMain[16-sizeSide], vreflo);
        break;
    }

    case 64:
    {
        int shuf_offset = intraPredAngleMode * 64;
        __m128i vshuf0 = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_64[shuf_offset + 0]);
        __m128i vshuf1 = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_64[shuf_offset + 16]);
        __m128i vshuf2 = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_64[shuf_offset + 32]);
        __m128i vshuf3 = _mm_loadu_si128((__m128i*) &intra_refbuild_shuffle_vectors_sidesize_64[shuf_offset + 48]);

        __m128i vref0 = _mm_loadu_si128((const __m128i*) &refSide[ 0 + 1]); // Offset ref by one to fit all necessary 16 refs. Offset accounted for in shuffle vectors.
        __m128i vref1 = _mm_loadu_si128((const __m128i*) &refSide[16 + 1]);
        __m128i vref2 = _mm_loadu_si128((const __m128i*) &refSide[32 + 1]);
        __m128i vref3 = _mm_loadu_si128((const __m128i*) &refSide[48 + 1]);

        // First quarter of references use references from vref3 only
        __m128i vrefout0 = _mm_shuffle_epi8(vref3, vshuf0);

        // Second quarter can require samples from vref3 and vref2
        __m128i vreftmp0 = _mm_shuffle_epi8(vref3, vshuf1);
        __m128i vreftmp1 = _mm_shuffle_epi8(vref2, vshuf1);
        __m128i vblend0 = _mm_cmpgt_epi8(vshuf1, _mm_set1_epi8(47));
        __m128i vrefout1 = _mm_blendv_epi8(vreftmp1, vreftmp0, vblend0);

        // Third quarter can require samples from vref3, vref2 and vref1
        vreftmp0 = _mm_shuffle_epi8(vref3, vshuf2);
        vreftmp1 = _mm_shuffle_epi8(vref2, vshuf2);
        __m128i vreftmp2 = _mm_shuffle_epi8(vref1, vshuf2);
        vblend0 = _mm_cmpgt_epi8(vshuf2, _mm_set1_epi8(47));
        __m128i vblend1 = _mm_cmpgt_epi8(vshuf2, _mm_set1_epi8(31));

        vreftmp0 = _mm_blendv_epi8(vreftmp1, vreftmp0, vblend0);
        __m128i vrefout2 = _mm_blendv_epi8(vreftmp2, vreftmp0, vblend1);

        // Fourth quarter can require samples from vref3, vref2, vref1 and vref0
        vreftmp0 = _mm_shuffle_epi8(vref3, vshuf3);
        vreftmp1 = _mm_shuffle_epi8(vref2, vshuf3);
        vreftmp2 = _mm_shuffle_epi8(vref1, vshuf3);
        __m128i vreftmp3 = _mm_shuffle_epi8(vref0, vshuf3);

        vblend0 = _mm_cmpgt_epi8(vshuf3, _mm_set1_epi8(47));
        vblend1 = _mm_cmpgt_epi8(vshuf3, _mm_set1_epi8(31));
        __m128i vblend2 = _mm_cmpgt_epi8(vshuf3, _mm_set1_epi8(15));

        vreftmp0 = _mm_blendv_epi8(vreftmp1, vreftmp0, vblend0);
        vreftmp0 = _mm_blendv_epi8(vreftmp2, vreftmp0, vblend1);
        __m128i vrefout3 = _mm_blendv_epi8(vreftmp3, vreftmp0, vblend2);

        _mm_storeu_si128((__m128i*) &refMain[-sizeSide],  vrefout0);
        _mm_storeu_si128((__m128i*) &refMain[16-sizeSide], vrefout1);
        _mm_storeu_si128((__m128i*) &refMain[32-sizeSide], vrefout2);
        _mm_storeu_si128((__m128i*) &refMain[48-sizeSide], vrefout3);
        break;
    }

    default:
        invAngle = invIntraAngTable[intraPredAngleMode];

        // Extend the Main reference to the left.
        for (int k = -sizeSide; k <= -1; k++)
        {
            refMain[k] = refSide[XIN_MIN((-k * invAngle + 256) >> 9, sizeSide)];
        }
    }
}

void Xin266FilterIntraNB_AVX2 (
    PIXEL *src,
    PIXEL *dst,
    UINT32 width,
    UINT32 height,
    SINT32 multiRefIdx)
{
    UINT32 length;
    UINT32 i;
    
    length = width*2 + height*2 + multiRefIdx*2 + 1;

    // first pixel (标量)
    dst[0] = src[0];

    // 主循环：一次处理 32 字节
    for (i = 1; i + 31 < length - 1; i += 32)
    {
        __m256i left   = _mm256_loadu_si256((__m256i*)(src + i - 1));
        __m256i center = _mm256_loadu_si256((__m256i*)(src + i));
        __m256i right  = _mm256_loadu_si256((__m256i*)(src + i + 1));

        // unpack to 16-bit
        __m256i left_lo   = _mm256_unpacklo_epi8(left, _mm256_setzero_si256());
        __m256i left_hi   = _mm256_unpackhi_epi8(left, _mm256_setzero_si256());
        __m256i center_lo = _mm256_unpacklo_epi8(center, _mm256_setzero_si256());
        __m256i center_hi = _mm256_unpackhi_epi8(center, _mm256_setzero_si256());
        __m256i right_lo  = _mm256_unpacklo_epi8(right, _mm256_setzero_si256());
        __m256i right_hi  = _mm256_unpackhi_epi8(right, _mm256_setzero_si256());

        // sum = left + 2*center + right + 2
        __m256i two = _mm256_set1_epi16(2);

        __m256i sum_lo = _mm256_add_epi16(left_lo, right_lo);
        sum_lo = _mm256_add_epi16(sum_lo, _mm256_slli_epi16(center_lo, 1));
        sum_lo = _mm256_add_epi16(sum_lo, two);
        sum_lo = _mm256_srli_epi16(sum_lo, 2);

        __m256i sum_hi = _mm256_add_epi16(left_hi, right_hi);
        sum_hi = _mm256_add_epi16(sum_hi, _mm256_slli_epi16(center_hi, 1));
        sum_hi = _mm256_add_epi16(sum_hi, two);
        sum_hi = _mm256_srli_epi16(sum_hi, 2);

        __m256i result = _mm256_packus_epi16(sum_lo, sum_hi);

        _mm256_storeu_si256((__m256i*)(dst + i), result);
        
    }

    // tail
    for (; i < length - 1; ++i)
    {
        dst[i] = (src[i-1] + (src[i]<<1) + src[i+1] + 2) >> 2;
    }

    // last pixel
    dst[length-1] = src[length-1];
    
}

